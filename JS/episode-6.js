// Demande trois nombres à l'utilisateur
let nombre1 = parseFloat(prompt("Entrez le premier nombre :"));
let nombre2 = parseFloat(prompt("Entrez le deuxième nombre :"));
let nombre3 = parseFloat(prompt("Entrez le troisième nombre :"));

// Calcule la moyenne des trois nombres
let moyenne = (nombre1 + nombre2 + nombre3) / 3;

// Affiche la moyenne
alert("La moyenne est : " + moyenne);
